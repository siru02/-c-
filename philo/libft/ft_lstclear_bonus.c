#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*tmp;

	tmp = *lst;
	while (tmp)
	{
		tmp = (*lst)->next;
		del((void *)(*lst)->content);
		free(*lst);
		*lst = tmp;
	}
	lst = 0;
}
